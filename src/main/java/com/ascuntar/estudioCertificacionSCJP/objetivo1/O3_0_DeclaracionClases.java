package com.ascuntar.estudioCertificacionSCJP.objetivo1;

public class O3_0_DeclaracionClases {
	
	/**
	
	Cuando escribimos codigo en Java, escribimos clases o interfaces. Dentro de las clases, como ya
	sabemos, estan las variables y los metodos (y alguna otra cosa mas). Como declaremos nuestras
	clases, metodos y variables afecta dramaticamente la conducta del codigo. Por ejemplo un metodo
	publico puede ser accedido desde el codigo ejecutado fuera de nuestra aplicacion, mientras que 
	uno privado no. Por este motivo, debemos estudiar la manera de declarar y modificar (o no) una
	clase.
	
	Antes de adentrarnos en las declaraciones de clases, vamos a hacer un rapido repaso a las normas
	asociadas con la declaracion de clases, y el uso de sentencias import y package en el codigo fuente.
	
		* Cada fichero codigo fuente debe tener una sola clase publica
		* Los comentarios pueden aparecer en cualquier linea de codigo
		* Si hay una clase publica en un fichero, el nombre del fichero debe coincidir con el nombre de
		  la clase publica.
		* Si la clase pertenece a un paquete, la sentencia "package" debe estar en la primera linea de
		  codigo, antes de cualquier sentencia "import" que haya.
		* No hay manera de declarar multiples clases en un fichero y que tengan diferentes paquetes, o usen
		  diferentes sentencias "import".
		* Un fichero puede tener mas de una clase no publica.
		* Un fichero que no tenga clase publica puede tener cualquier nombre, no debe coincidir con el nombre
		  de las clases que contiene.
		


	 */

}
