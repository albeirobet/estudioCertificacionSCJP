package com.ascuntar.estudioCertificacionSCJP.objetivo1;

abstract class O3_2_ClaseAbstracta {

	/**
	 Cuando una clase se declara como Abstracta esta NUNCA podra ser instanciada. Su unico
	 objetivo es ser extendida. 
	 Los metodos marcados como abstractos acaban en ";" y no en llaves "{}".
	 Hay que tener en cuenta que si dentro de una clase hay un metodo definido como abstracto
	 esto obliga a que la clase sea tambien abstracta. Sin embargo, podemos poner metodos no abstractos
	 en una clase abstracta. Si necesitamos tener metodos con implemantaciones que no deberian cambiar
	 de un tipo de Objeto a otro esta implementacion es la correcta. Al tener metodos abstractos en una
	 clase abstracta, las subclases de la clase abstracta heredan los metodos implementados. La buena
	 noticia es que las subclases heredan funcionalidad, y necesitan implementar solo los metodos que
	 definen el comportamiento especifico de la subclase.
	 NO podemos marcar una clase como abstracta y final. Porque tienen significados opuestos, una clase
	 abstracta debe ser extendida por otras clases mientras que una final no tendra clases que extiendan
	 de ella. 
	  
	  
	 */
	
}
