package com.ascuntar.estudioCertificacionSCJP.objetivo1;

public class O7_1_Declaracion_Variables {
	
	/**
	Hay dos tipos de variables en Java:
	
	* Primitivas. Un tipo primitivo puede ser uno de los ocho tipos: 
		          char, boolean, byte, short, int, long, double, float.
		          Una vez el primitivo haya sido declarado, ese tipo primitivo nunca cambia, aunque
		          en la mayoria de los casos cambia de valor.
	* Variables de Referencia. Una variable de referencia es usada para referirse (o acceder) a un
      objeto. Una referencia esta declarada para ser de un tipo especifico y nunca puede cambiar. Una
      referencia puede ser usuada para referirse a cualquier tipo especifico, o de un subtipo del
      tipo declarado (un tipo compatible). Hablamos mas de esto despues al hablar de polimorfismo
	 */

	int numero; //Variable primitiva
	String nombre; //Variable de referencia al Objeto String
}
